#include "../includes/push_swap.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*d;

	d = (char *)b;
	i = 0;
	while (i < len)
	{
		d[i] = c;
		i++;
	}
	b = d;
	return (b);
}
