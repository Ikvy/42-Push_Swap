#include "../includes/push_swap.h"

int	ft_putstrv2(char *x)
{
	int	i;

	i = 0;
	if (!x)
		i += ft_putstrv2("(null)");
	else
	{
		while (x[i])
		{
			ft_putcharv2(x[i]);
			i++;
		}
	}
	return (i);
}
