#include "../includes/push_swap.h"

int	ft_putnbru2(unsigned int x)
{
	int	c;

	c = 0;
	if (x >= 0 && x <= 9)
		c += ft_putcharv2(x + '0');
	else
	{
		c += ft_putnbrv2(x / 10);
		c += ft_putnbrv2(x % 10);
	}
	return (c);
}
